#include <iostream>
using namespace std;

int main()
{
	cout<<"\n size of int: "<<sizeof(int);
	cout<<"\n size of float: "<<sizeof(float);
	cout<<"\n size of char: "<<sizeof(char);
	cout<<"\n size of double: "<<sizeof(double);
	return 0;
}


